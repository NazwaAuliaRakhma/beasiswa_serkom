<h1>Beasiswa B</h1>
<p>Beasiswa B ditujukan untuk mahasiswa yang memiliki prestasi luar biasa di bidang non-akademik, seperti olahraga, seni, atau kegiatan sosial.</p>
<p><strong>Syarat:</strong></p>
<ul>
    <li>Mahasiswa aktif yang terlibat dalam kegiatan ekstrakurikuler.</li>
    <li>Menunjukkan prestasi di bidang yang dipilih.</li>
    <li>Melampirkan bukti prestasi (sertifikat, medali, dsb.).</li>
</ul>
<p><strong>Cara Mendaftar:</strong></p>
<p>Calon penerima beasiswa harus mengisi formulir pendaftaran dan melampirkan:</p>
<ul>
    <li>Bukti prestasi di bidang non-akademik.</li>
</ul>
<p><strong>Batas Waktu Pendaftaran:</strong> 30 Oktober 2024</p>
