<h1>Beasiswa C</h1>
<p>Beasiswa C ditujukan untuk anak karyawan Telkom yang berprestasi dan membutuhkan bantuan biaya kuliah.</p>
<p><strong>Syarat:</strong></p>
<ul>
    <li>Merupakan anak dari karyawan aktif Telkom.</li>
    <li>Memiliki prestasi akademik yang baik.</li>
    <li>Menyerahkan dokumen yang membuktikan status sebagai anak karyawan.</li>
</ul>
<p><strong>Cara Mendaftar:</strong></p>
<p>Calon penerima beasiswa harus mengisi formulir pendaftaran dan melampirkan:</p>
<ul>
    <li>Kartu keluarga.</li>
</ul>
<p><strong>Batas Waktu Pendaftaran:</strong> 30 Oktober 2024</p>
